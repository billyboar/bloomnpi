package helpers

import (
	"net/http"
	"io"
	"os"
)

func Download(file string) error {
	out, err := os.Create("data/" + file + ".zip")
	if err != nil {
		return err
	}
	defer out.Close()

	resp, err := http.Get("http://download.cms.gov/nppes/" + file + ".zip")
	if err != nil {
		return err
	}
	defer resp.Body.Close()

	_, err = io.Copy(out, resp.Body)
	if err != nil {
		return err
	}

	return nil
}
